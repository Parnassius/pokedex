@pytest.mark.parametrize(
    "table, identifier_column, changes",
    [
        (tables.AbilityName, tables.AbilityName.ability_identifier, ability_changes),
        (tables.ItemName, tables.ItemName.item_identifier, item_changes),
        (tables.MoveName, tables.MoveName.move_identifier, move_changes),
        (tables.NatureName, tables.NatureName.nature_identifier, nature_changes),
        (
            tables.PokemonFormName,
            tables.PokemonFormName.pokemon_identifier,
            pokemon_changes,
        ),
        (
            tables.PokemonSpeciesName,
            tables.PokemonSpeciesName.pokemon_species_identifier,
            pokemon_species_changes,
        ),
        (tables.StatName, tables.StatName.stat_identifier, stat_changes),
        (tables.TypeName, tables.TypeName.type_identifier, type_changes),
    ],
)
def test_names_changes(
    table: type[tables.Base],
    identifier_column: InstrumentedAttribute[str],
    changes: ChangesDict,
) -> None:
    with pokedex.session() as session:
        stmt = (
            select(
                identifier_column.label("identifier"),
                table.language_identifier,  # type: ignore[attr-defined]
                table.game_group_identifier,  # type: ignore[attr-defined]
                table.name,  # type: ignore[attr-defined]
            )
            .select_from(table)
            .join(tables.GameGroup)
            .order_by(
                identifier_column,
                table.language_identifier,  # type: ignore[attr-defined]
                tables.GameGroup.order,
            )
        )
        result = session.execute(stmt)

        identifier = ""
        language = Language.get_default()
        name: str
        for row in result:
            if row.identifier != identifier or row.language_identifier != language:
                identifier = row.identifier
                language = row.language_identifier
                name = row.name
            name_change = (
                changes.get(language, {})
                .get(row.game_group_identifier, {})
                .get(identifier, None)
            )
            if row.name.casefold() == name.casefold():
                assert name_change is None, (
                    table,
                    identifier,
                    language,
                    row.game_group_identifier,
                )
            else:
                assert name_change == (name, row.name), (
                    table,
                    identifier,
                    language,
                    row.game_group_identifier,
                )
            name = row.name
